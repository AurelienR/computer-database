package com.excilys.cdb.utils;

public class DateFormatManagerException extends RuntimeException {

  private static final long serialVersionUID = 1L;

  public DateFormatManagerException(String message) {
    super(message);
  }

  public DateFormatManagerException(String message, Throwable cause) {
    super(message, cause);
  }

  public DateFormatManagerException(Throwable cause) {
    super(cause);
  }
}